                 "After its transit through the endoplasmic reticulum , Rac1 moves back to the cytosol where it stays in an inactive reservoir until the reception of extracellular signals by the cell .",
                 "This pool is stabilized by the formation of stoichiometric complexes with RhoGDIs ( , step 2 ) .",
                 "RhoGDIs perform both negative and positive actions in this complex .",
                 "On the negative side , they inhibit the usually high intrinsic GDP and GTP exchange of Rac1 , thus favoring the maintenance of the bound GTPase in the inactive conformation in non stimulated cells .",
                 "This function is mediated by the direct interaction of the RhoGDI molecule with the Rac1 switch regions .",
                 "In addition , they use a deep hydrophobic cavity to trap the Rac1 prenyl group , a mechanism that keeps the GTPase away from the membrane in a cytosolic , fully soluble pool ( ) .",
                 "On the positive side , RhoGDIs protect the bound Rac1 molecules from proteolytic degradation and , in addition , are likely involved in the final transit of Rho and Rac GTPases to the plasma membrane .",
                 "The cytosolic reservoir of Rac1 is rapidly mobilized to the plasma membrane upon cell stimulation ( , step 3a ) or , alternatively , to the nucleus in the G 2 phase of the cell cycle ( , step 3b ) .",
                 "The main factors involved in the former mobilization route are the Rac1 GEFs , a group of enzymes containing either Dbl-homology or Dock-homology domains that promote the rapid transition of Rac1 from the inactive ( GDP bound ) to the active ( GTP bound ) state ( , step 4 ) .",
                 "However , alternative physiological activation steps exist , such as the transglutaminase dependent serotonylation of Rac1 downstream of G coupled receptors .",
                 "This mobilization step is also facilitated by the C-terminal Rac1 polybasic and proline rich regions .",
                 "The former region favors the formation of hydrophilic interactions with the negatively charged heads of lipids only present in the plasma membrane , because Rac related proteins lacking this hydrophilic signal are localized in intracellular vesicles , ( , step 3a ) .",
                 "The Rac1 proline rich region interacts with the SH3 region of ArhGEF7 ( also known as beta-Pix ) , a Dbl-homology family protein that can associate with F-actin rich areas of the cell ( , step 3a ) .",
                 "Recent data indicate that other ancillary signals collaborate in this process , including the post-translational modification of Rac1 by phosphorylation , sumoylation and ubiquitination .",
                 ", Rac1 localization can be also regulated positively by extrinsic signals such as the presence of specific phospholipids ( PtdIns ( 4,5 ) P 2 , PtdIns ( 3,4,5 ) P 3 ) , and lipid rafts - at the plasma membrane ( , step 3a ) .",
                 "At the end of the effector phase ( , step 5 ) , the standard regulatory model holds that Rac1 goes back to the GDP bound state via the action of Rho and Rac GTPase activating proteins ( , step 6a ) , re-associates with RhoGDI molecules ( , step 6b ) and moves back to the inactive cytosolic reservoir until a new stimulation cycle starts .",
                 "In addition to this standard model , recent data demonstrated that Rac1 can undergo activation and inactivation cycles by shuttling between the plasma membrane and endocytic compartments .",
                 ", The shuttling of Rac1 in and out of the nucleus requires the Rac1 polybasic region and karyopherin alpha2 , an importin that transfers cargo molecules inside the nucleus ( , step 3b ) .",
                 "Whether this step requires GEFs , carrier proteins , or intracellular docking proteins is unknown as yet .",
                 "The mechanism by which Rac1 is returned to the cytosol remains ill defined ( , step 6c ) .",
                 "Whereas the GTPase and RhoGDI complex and the catalytic steps involved in Rac1 GDP and GTP exchange and GTP hydrolysis are well understood in structural terms , , the dynamic aspects that modulate the release of Rac1 from RhoGDIs during the activation process are not well understood .",
                 "For instance , we do not know whether Rac1 dissociates from the RhoGDI before contacting the upstream GEFs or , alternatively , whether the GEFs promote both the dissociation and activation of the GTPase .",
                 "In either case , it is unclear the intracellular cues used by either Rac1 or the Rac1 and RhoGDI complex to reach the subcellular regions where the stimulated GEFs are localized .",
                 "Finally , we do not know whether these Rac1 mobilization related steps are general and and or cell type specific .",
                 "Recent inroads in this subject suggest that the release of Rac1 is mediated by the direct phosphorylation of RhoGDI by either upstream ( Src , protein kinase C ) or downstream ( Pak1 ) kinases .",
                 "- In any case , it is still difficult to explain how the cytosolic RhoGDI and Rac1 complexes manage to get close to those kinases during cell stimulation .",
                 "To shed light on this process , we decided to search for proteins involved in the regulation of the translocation of Rac1 to the plasma membrane using a genome-wide functional screen ( ) .",
                 "This approach led to the isolation of coronin1A ( Coro1A ) as a protein capable of inducing the translocation and activation of Rac1 during cell signaling ( , bottom ) .",
                 "Coro1A belongs to a large family of cytoskeletal regulators that show a phylogenetic distribution from unicellular eukaryotes to humans .",
                 "These proteins control the bundling of F-actin filaments , the growth and orientation angle of new F-actin branches , and the disassembly of old actin cables .",
                 "Such activities suggested to us that Coro1A could be possibly involved in a cross-talk between the F-actin cytoskeleton and the process of Rac1 translocation and and or activation .",
                 "Consistent with this idea , we could demonstrate that the overexpression of Coro1A induced the F-actin-dependent translocation and activation of Rac1 in the plasma membrane .",
                 "Conversely , its inactivation led to ineffective activation of Rac1 and Rac1-downstream routes .",
                 "This pathway was active in a number of cell types ( COS1 , 293T , Jurkat cells ) and cellular stimulation conditions ( EGF signaling , integrin mediated adhesion , T-cell receptor stimulation ) .",
                 "Figure2 .",
                 "Scheme of the cellomic screen used in the work reviewed here .",
                 "The first screening was conducted using an expression library of 135,000 independent cDNA clones obtained from human T cells .",
                 "To this end , we used a reporter HEK293T stably expressing a cytoplasmic EGFP-Rac1 protein ( step 1 ) .",
                 "To increase the efficiency of the screening , this cell line also expressed anti-apoptotic proteins to avoid the loss of clones due to the presence of pro-apoptotic molecules in the transfected cDNA pools .",
                 "The screening was conducted by transfecting separate pools of 90 cDNAs in the reporter cell line using the calcium phosphate precipitation method ( step 2 ) and the subsequent score of cells showing plasma membrane localized EGFP-Rac1 using epifluorescence microscopy ( step 3 ) .",
                 "Positive pools were progressively subdivided and transfected in the reporter cell line ( steps 4a and 4b ) until the isolation of the cDNA clones responsible for the Rac1 translocating activity ( step 5 ) .",
