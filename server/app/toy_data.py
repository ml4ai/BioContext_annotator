                 "Mechanistic studies revealed that Coro1A promotes the translocation and activation of Rac1 via the formation of a F-actin-dependent , heteromolecular complex with ArhGEF7 , Pak1 , RhoGDI and Rac1 .",
                 "According to our proposed model ( ) , the formation of this Rac1 translocation complex requires upstream signals such as Rac1 activity , aggregated lipid rafts in the plasma membrane and the presence of F-actin in cells ( , stage 1 ) .",
                 "In the absence of those upstream signals , Coro1A can only associate in a stable manner with ArhGEF7 in the cytoplasm ( , stage 2 ) .",
                 "Upon conditions triggering F-actin polymerization , this inert complex is tethered to membrane ruffles and lamellipodia , a process made it possible by the intrinsic F-actin binding properties of Coro1A ( , stage 3 ) .",
                 "The F-actin bound complex binds then Pak1 and Rac1 and RhoGDI ( , stage 4 ) , leading to the phosphorylation of RhoGDI by Pak1 , the release of Rac1 from the phosphorylated RhoGDI and , finally , to Rac1 activation ( , stage 5 ) .",
                 "Several properties of this new translocation process are important from a signaling point of view .",
                 "First , the F-actin binding properties of Coro1A offer a rather simple explanation for understanding how the cytosolic Rac1 and RhoGDI pool is shuttled toward the plasma membrane during cell stimulation conditions .",
                 "Second , its dependency on upstream Rac1 signals suggest that the Coro1A route is involved in the generation of secondary waves of Rac1 activation rather than being directly involved in the initial burst of Rac1 activity that takes place upon the reception of the extracellular signal .",
                 "Thus , according to our proposed regulatory model , cells will have to trigger Rac1 or RhoG activation in order to engage the downstream Coro1A dependent relay mechanism ( ) .",
                 "Finally , the need of pre-formed F-actin cytoskeletal structures to engage the Coro1A dependent translocation route suggests that F-actin can induce a positive feed-back mechanism that will allow the generation of additional waves of Rac1 activation and F-actin polymerization at the plasma membrane ( ) .",
