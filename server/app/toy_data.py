                 "Despite these advances , the role of Coro1A in the translocation and activation of Rac1 is far from being an open and shut case .",
                 "Thus , the mechanism that regulates the assembly of Pak1 and the RhoGDI and Rac1 pair onto the Coro1A and ArhGEF7 complex has not been fully elucidated yet .",
                 "Our data indicate that such assembly requires the presence of ArhGEF7 in the Coro1A complex , a result consistent with the known physical interaction between ArhGEF7 and Pak1 .",
                 "However , the requirement of F-actin for the formation of the entire Coro1A/Pak1/RhoGDI complex also indicates that additional ancillary partners and and or signals must also contribute to this process .",
                 "Whether these extra elements are proteins , membrane lipids , and and or other upstream signals remains to be determined .",
                 "It is also unclear the Rac1 GEF in charge of activating Rac1 upon its release from the Coro1A nucleated protein complex ( ) .",
                 "One option is ArhGEF7 itself , since it is obvious that its presence in the Coro1A complex will ensure its close proximity with the released Rac1 molecules ( ) .",
                 "However , the catalytic activity of ArhGEF7 is controversial , suggesting that other GEFs could be involved ( , protein labeled as X ) .",
                 "It is possible therefore that such activation step is at the hands of other GEFs that , due to their localization in F-actin cables or the plasma membrane , could be in close proximity to the Coro1A nucleated complex .",
                 "The resolution of all those lingering issues will need further experimental work in the near future .",
                 "It is also worth noting that recent data obtained in our laboratory suggest that Coro1A can use alternative mechanisms to induce the translocation of Rac1 to the plasma membrane .",
                 "Hence , we have observed that Coro1A can trigger the membrane localization of Rac1 R66E , a mutant protein that can not bind RhoGDI .",
                 "This alternative route is mechanistically distinct from that characterized in this reviewed publication , because it can not be inhibited by blocking Pak1 function ( Castro-Castro A , Bustelo XR , unpublished observations ) .",
                 "Although these results are probably not physiologically relevant given the lack of significant amounts of RhoGDI-free Rac1 proteins in the cytosol , they are interesting because they reveal a pathway that can further enhance the membrane anchoring of Rac1 upon its liberation from the RhoGDI complexes by the Coro1A/ArhGEF7/Pak1 complex .",
                 "Although this alternative translocation mechanism remains to be elucidated , we surmise that it could involve the generation of F-actin-dependent \" permissive \" conditions at the plasma membrane .",
                 "Consistent with this view , we have observed that the overexpression of Coro1A induces lipid raft aggregation in the plasma membrane .",
                 "Furthermore , we found that the aggregation of lipid rafts promotes the translocation of the Rac1 R66E mutant to the plasma membrane ( Castro-Castro A , Bustelo XR , unpublished observations ) .",
                 "Finally , it should be pointed out that Coro1A and ArhGEF7 are not ubiquitously expressed , so it is unlikely that the Rac1 translocation and activation mechanism reviewed here will be utilized by all cell types .",
                 "In this context , it will be interesting to investigate whether other cytoskeletal proteins fulfill Coro1A like functions in cells that do not express Coro1A .",
                 "This is a feasible scenario , because our genome-wide functional screen has resulted in the identification of a second WD40 family protein ( WDR26 ) that can also induce the translocation of Rac1 to the plasma membrane ( Castro-Castro A , Bustelo XR , unpublished data ) .",
                 "It is also possible that the increase in the membrane localized pool of Rac1 can be achieved by routes alternative to those reported here .",
                 "For example , our cellomic screen has also identified transmembrane proteins that favor the membrane localization of Rac1 by blocking the internalization of lipid rafts .",
                 "Unlike the case of Coro1A dependent route , this alternative pathway can not be inhibited by either Rac1 dominant negative mutants or Pak1 interference strategies .",
                 "Taken together , these results suggest that cells can resort to many pathways to regulate the translocation of Rac1 during cell signaling .",
